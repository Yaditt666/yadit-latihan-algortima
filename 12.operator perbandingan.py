#operator perbandingan
x = 7
y = 5

#lebih dari (>)
print('')
hasil = x > y
print('x > y =', hasil)

#lebih kecil (<)
print('')
hasil = x < y
print('x < y =', hasil)

#lebih besar sama dengan (>=)
print('')
hasil = x >= y
print('x >= y =', hasil)

#lebih kecil sama dengan (<=)
print('')
hasil = x <= y
print('x <= y =', hasil)

#sama dengan ==
print('')
hasil = x == y
print('x == y =', hasil)

#tidak sama dengan !=
print('')
hasil = x != y
print('x != y =', hasil)

