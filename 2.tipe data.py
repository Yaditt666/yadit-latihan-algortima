# a = 10, a adalah variabel dengan nilai 10

# tipe data: angka satuan( integer)
data_integer = 100
print("data : ", data_integer)
print("-bertipe : ", type(data_integer))

#tipe data: angka dengan koma(float)
data_float = 3.98
print("data : ", data_float)
print("-bertipe : ", type(data_float))

#tipe data: kumpulan karakter (string)
data_string = "yadit"
print("data : ", data_string)
print("-bertipe : ", type(data_string))

#tipe data: biner true/false (boolean)
data_bool = True
print("data : ", data_bool)
print("-bertipe : ", type(data_bool)) 

#tipe data: bilangan kompleks (complex)
data_complex = complex(8,9)
print("data : ", data_complex)
print("-bertipe : ", type(data_complex))

#Tipe Data List
data_list = ["satu","dua","tiga","satu"]
print("data : ", data_list)
print("-bertipe : ", type(data_list))
 
#Tipe Data Tuple
data_tuple = ("satu","dua","tiga","satu")
print("data : ", data_tuple)
print("-bertipe : ", type(data_tuple))
 
#Tipe Data Set
data_set = {"satu","dua","tiga","empat"}
print("data : ", data_set)
print("-bertipe : ", type(data_set))
 
#Tipe Data Dictionary
data_dictionary = {"satu":1, "dua":2.13, "tiga":"a", "empat": True}
