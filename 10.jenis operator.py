#jenis-jenis operator dalam python
# 1. Operator Aritmatika
print("1.operator Aritmatika")
x = 68
y = 12
print("Tambah:", x + y)            
print("Kurang:", x - y)           
print("Kali:", x * y)             
print("Bagi:", x / y)             
print("Pembagian Bulat:", x // y)  
print("Sisa Bagi:", x % y)        
print("Pangkat:", x ** y)  
print('')        

# 2. Operator Perbandingan
print("2.operator perbandingan")
print("x > y:", x > y)             
print("x < y:", x < y)             
print("x == y:", x == y)          
print("x != y:", x != y)           
print("x >= y:", x >= y)           
print("x <= y:", x <= y) 
print('')          

# 3. Operator Logika
print("3.operator logika")
a = True
b = False
print("a and b:", a and b)        
print("a or b:", a or b)          
print("not a:", not a)    
print('')         

# 4. Operator Penugasan
print("4.operator penugasan")
z = 12
print("Nilai awal z:", z)
z += 4                           
print("z += 5:", z)              
z -= 5                           
print("z -= 3:", z)              
z *= 3                           
print("z *= 2:", z)             
z /= 6                          
print("z /= 4:", z) 
print('')             

# 5. Operator Bitwise
print("5.operator bitwise")
num1 = 6     
num2 = 3      
print("num1 & num2:", num1 & num2)  
print("num1 | num2:", num1 | num2)  
print("num1 ^ num2:", num1 ^ num2)  
print("~num1:", ~num1)              
print("num1 << 1:", num1 << 1)      
print("num1 >> 1:", num1 >> 1)   
print('')   

# 6. Operator Keanggotaan
print("6.operator keanggotaan")
animals = ["kucing", "anjing", "kelinci"]
print("'kucing' in animals:", 'kucing' in animals)   
print("'ular' in animals:", 'ular' in animals) 
print('')       

# 7. Operator Identitas
print("7.operator indentitas")
var1 = 10
var2 = 10
print("var1 is var2:", var1 is var2)     
print("var1 is not var2:", var1 is not var2) 

# Contoh lainnya untuk operator identitas
list1 = [1, 2, 3]
